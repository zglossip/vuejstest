        assert(pokemonType1.offense.fire === 1)
        assert(pokemonType1.offense.water === 1)
        assert(pokemonType1.offense.grass === 1)
        assert(pokemonType1.offense.electric === 1)
        assert(pokemonType1.offense.steel === 1)
        assert(pokemonType1.offense.psychic === 1)
        assert(pokemonType1.offense.ice === 1)
        assert(pokemonType1.offense.dragon === 1)
