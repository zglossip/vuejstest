        assert(pokemonType1.defense.fire === 1)
        assert(pokemonType1.defense.water === 1)
        assert(pokemonType1.defense.grass === 1)
        assert(pokemonType1.defense.electric === 1)
        assert(pokemonType1.defense.steel === 1)
        assert(pokemonType1.defense.psychic === 1)
